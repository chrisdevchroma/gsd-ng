#!/usr/bin/env node
'use strict';
// Generate gsd-ng/bin/lib/confusables.cjs from a local copy of Unicode TR39 confusables.txt.
// Source: https://www.unicode.org/Public/security/latest/confusables.txt (download manually).
// Usage: node gsd-ng/scripts/generate-confusables.cjs path/to/confusables.txt
//
// Filter: keep only MA-table rows whose target is a single codepoint in
// U+0061..U+007A (Latin lowercase a-z) or U+0041..U+005A (Latin uppercase A-Z).
// Output is committed; runtime never re-parses TR39 data.

const fs = require('fs');
const path = require('path');
const { spawnSync } = require('child_process');

const CONFUSABLES_VERSION = '17.0.0';
const OUTPUT_PATH = path.join(
  __dirname,
  '..',
  'gsd-ng',
  'bin',
  'lib',
  'confusables.cjs',
);

const TARGET_CODEPOINTS = new Set();
for (let cp = 0x0061; cp <= 0x007a; cp++) TARGET_CODEPOINTS.add(cp);
for (let cp = 0x0041; cp <= 0x005a; cp++) TARGET_CODEPOINTS.add(cp);

function main() {
  const inputPath = process.argv[2];
  if (!inputPath) {
    console.error(
      'Usage: node generate-confusables.cjs path/to/confusables.txt',
    );
    process.exit(2);
  }
  const text = fs.readFileSync(inputPath, 'utf8');
  const map = {};

  for (const rawLine of text.split('\n')) {
    const line = rawLine.replace(/^﻿/, '');
    if (!line || line.startsWith('#')) continue;
    // TR39 confusables.txt format: "<SRC> ; <TGT> ; MA  # ( <visual> → <visual> ) <names>"
    // We only accept single-codepoint targets (no spaces between target hex tokens).
    const m = line.match(/^([0-9A-Fa-f]+)\s*;\s*([0-9A-Fa-f]+)\s*;\s*MA\b/);
    if (!m) continue;
    const srcCp = parseInt(m[1], 16);
    const tgtCp = parseInt(m[2], 16);
    if (!TARGET_CODEPOINTS.has(tgtCp)) continue;
    const srcChar = String.fromCodePoint(srcCp);
    const tgtChar = String.fromCodePoint(tgtCp);
    // Skip identity mapping (a -> a) if any
    if (srcChar === tgtChar) continue;
    map[srcChar] = tgtChar;
  }

  // Deterministic key order: sort by source codepoint
  const sortedEntries = Object.entries(map).sort(
    (a, b) => a[0].codePointAt(0) - b[0].codePointAt(0),
  );
  const sortedMap = {};
  for (const [k, v] of sortedEntries) sortedMap[k] = v;

  // Emit prettier-compatible source (single quotes, trailing commas) so the
  // committed file passes `npm run format:check` without a separate pass.
  function jsString(s) {
    // Single-quoted JS string. Escape backslashes, single-quotes, and control
    // chars; preserve non-ASCII codepoints verbatim (matches prettier output).
    let out = "'";
    for (const ch of s) {
      const cp = ch.codePointAt(0);
      if (ch === '\\') out += '\\\\';
      else if (ch === "'") out += "\\'";
      else if (cp === 0x08) out += '\\b';
      else if (cp === 0x09) out += '\\t';
      else if (cp === 0x0a) out += '\\n';
      else if (cp === 0x0c) out += '\\f';
      else if (cp === 0x0d) out += '\\r';
      else if (cp < 0x20) out += '\\u' + cp.toString(16).padStart(4, '0');
      else out += ch;
    }
    return out + "'";
  }

  const banner =
    '// Generated by gsd-ng/scripts/generate-confusables.cjs from Unicode TR39 confusables.txt.\n' +
    '// Source: https://www.unicode.org/Public/security/latest/confusables.txt\n' +
    `// Unicode version: ${CONFUSABLES_VERSION}\n` +
    '// Regenerate via: node gsd-ng/scripts/generate-confusables.cjs path/to/confusables.txt\n' +
    '// DO NOT EDIT BY HAND — regenerate from upstream TR39 data.\n';

  let mapBody = '{\n';
  for (const [k, v] of sortedEntries) {
    mapBody += `    ${jsString(k)}: ${jsString(v)},\n`;
  }
  mapBody += '  }';

  const body =
    "'use strict';\n\n" +
    'module.exports = {\n' +
    `  CONFUSABLES_VERSION: ${jsString(CONFUSABLES_VERSION)},\n` +
    `  CONFUSABLES_MAP: ${mapBody},\n` +
    '};\n';

  fs.writeFileSync(OUTPUT_PATH, banner + body, 'utf8');

  // Format with project's prettier so committed output matches `npm run format:check`.
  // Required: regenerated output must satisfy the format-check gate in scripts/run-tests.cjs.
  const prettierBin = path.join(
    __dirname,
    '..',
    'node_modules',
    '.bin',
    'prettier',
  );
  if (fs.existsSync(prettierBin)) {
    const r = spawnSync(prettierBin, ['--write', OUTPUT_PATH], {
      stdio: 'inherit',
    });
    if (r.status !== 0) {
      console.error('prettier --write failed on', OUTPUT_PATH);
      process.exit(r.status || 1);
    }
  } else {
    console.warn(
      'prettier not found at',
      prettierBin,
      '— output may fail format:check.',
    );
  }

  console.log(
    `Wrote ${OUTPUT_PATH} — ${Object.keys(sortedMap).length} entries (Unicode ${CONFUSABLES_VERSION}).`,
  );
}

main();
